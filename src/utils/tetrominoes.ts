export function createTetromino(type: TetrominoType, rotation: number = 0): Tetromino {
  const shapes = TETROMINO_SHAPES[type];
  const shape = shapes[rotation % shapes.length];
  
  return {
    type,
    shape,
    position: { x: Math.floor(10 / 2) - Math.floor(shape[0].length / 2), y: 0 },
    color: TETROMINO_COLORS[type],
  };
}

export function rotateTetromino(tetromino: Tetromino): Tetromino {
  const shapes = TETROMINO_SHAPES[tetromino.type];
  const currentIndex = shapes.findIndex(shape => 
    JSON.stringify(shape) === JSON.stringify(tetromino.shape)
  );
  const nextIndex = (currentIndex + 1) % shapes.length;
  
  return {
    ...tetromino,
    shape: shapes[nextIndex],
  };
}

export function getRandomTetrominoType(): TetrominoType {
  const types: TetrominoType[] = ['I', 'O', 'T', 'S', 'Z', 'J', 'L'];
  return types[Math.floor(Math.random() * types.length)];
}